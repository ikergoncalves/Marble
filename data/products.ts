/** Deterministic placeholder cover image for a product. */
function cover(slug: string): string {
  return `https://picsum.photos/seed/${slug}/800/600`;
}

/** Deterministic placeholder preview gallery for a product. */
function previews(slug: string, count: number): string[] {
  return Array.from(
    { length: count },
    (_, index) => `https://picsum.photos/seed/${slug}-${index + 1}/800/600`,
  );
}

